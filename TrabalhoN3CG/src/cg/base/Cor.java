package cg.base;

public class Cor {
	
	private double R;
	private double G;
	private double B;
	
	public Cor(double r, double g, double b) {
		super();
		R = r;
		G = g;
		B = b;
	}
	
	public double getR() {
		return R;
	}
	public void setR(double r) {
		R = r;
	}
	public double getG() {
		return G;
	}
	public void setG(double g) {
		G = g;
	}
	public double getB() {
		return B;
	}
	public void setB(double b) {
		B = b;
	}

}
